import streamlit as st

def ocean_ammonia_emissions():
    # Set page title
    st.write("### Ammonia Emissions from Ocean Processes")

    # Introduction
    st.write("""
    Ammonia emissions from ocean processes primarily result from natural biological and chemical processes in marine environments. Ammonia (NH3) is a compound composed of nitrogen and hydrogen, and its presence in oceans plays a crucial role in nutrient cycling and marine ecosystems. However, excessive ammonia emissions can lead to environmental imbalances and impact marine life.
    """)

    # Sources of Ammonia Emissions
    st.write("### Sources of Ammonia Emissions from Ocean Processes")
    st.write("#### Marine Organisms:")
    st.write("""
    Marine organisms, such as phytoplankton, zooplankton, and bacteria, produce ammonia as a metabolic byproduct during biological processes, such as nitrogen metabolism and excretion.
    """)
    st.write("#### Decomposition of Organic Matter:")
    st.write("""
    Decomposition of organic matter, including dead marine organisms and fecal matter, releases ammonia into the surrounding water column.
    """)
    st.write("#### Natural Chemical Processes:")
    st.write("""
    Ammonia can also be generated through natural chemical processes, such as the hydrolysis of organic nitrogen compounds and the reduction of nitrate ions by anaerobic bacteria in marine sediments.
    """)

    # Types of Ammonia Emissions
    st.write("### Types of Ammonia Emissions from Ocean Processes")
    st.write("#### Dissolved Ammonia:")
    st.write("""
    Dissolved ammonia refers to ammonia molecules that are present in the water column in a dissolved form. It can originate from biological processes, as well as from the breakdown of organic matter.
    """)
    st.write("#### Atmospheric Ammonia Deposition:")
    st.write("""
    Atmospheric ammonia can deposit into the ocean surface through air-sea exchange processes, contributing to dissolved ammonia concentrations in seawater.
    """)

    # Environmental Impacts
    st.write("### Environmental Impacts of Ammonia Emissions from Ocean Processes")
    st.write("#### Algal Blooms:")
    st.write("""
    Excessive ammonia concentrations in seawater can promote the growth of algae, leading to harmful algal blooms (HABs). These blooms can deplete oxygen levels in the water and harm marine ecosystems.
    """)
    st.write("#### Eutrophication:")
    st.write("""
    Ammonia enrichment in coastal waters can contribute to eutrophication, a process characterized by excessive nutrient levels that stimulate algal growth and lead to oxygen depletion and ecosystem degradation.
    """)

    # Preventive Measures
    st.write("### Preventive Measures to Reduce Ammonia Emissions from Ocean Processes")
    st.write("#### Reducing Nutrient Runoff:")
    st.write("""
    Implementing measures to reduce nutrient runoff from land-based sources, such as agricultural runoff and wastewater discharge, can help minimize ammonia input into coastal waters.
    """)
    st.write("#### Managing Wastewater Discharges:")
    st.write("""
    Treating wastewater to remove nitrogen compounds before discharge into coastal waters can reduce ammonia emissions and mitigate eutrophication.
    """)
    st.write("#### Monitoring and Regulation:")
    st.write("""
    Implementing monitoring programs and regulations to control ammonia emissions from industrial activities, such as aquaculture and coastal development, can help prevent environmental damage.
    """)
    st.write("#### Promoting Sustainable Practices:")
    st.write("""
    Encouraging sustainable fishing practices and coastal development strategies that minimize nutrient inputs and protect marine ecosystems can contribute to reducing ammonia emissions from ocean processes.
    """)

ocean_ammonia_emissions()
