import streamlit as st

st.title("Major Sources of Ammonia Emissions")

st.write("""
Ammonia emissions arise from various human activities and natural processes. Some of the major sources include:
""")

st.subheader("Agricultural Activities:")

st.write("""
1. **Livestock Farming:** Animal waste and urine contribute to ammonia emissions, particularly from ruminant animals such as cattle, sheep, and goats.
2. **Fertilizer Application:** The application of nitrogen-based fertilizers in agriculture leads to ammonia volatilization from the soil.
""")

st.subheader("Industrial Processes:")

st.write("""
1. **Chemical Manufacturing:** Ammonia is produced as a byproduct in chemical manufacturing processes, including the production of fertilizers, explosives, and synthetic materials.
2. **Wastewater Treatment:** Anaerobic decomposition of organic matter in sewage treatment facilities generates ammonia as a byproduct.
""")

st.subheader("Household and Commercial Sources:")

st.write("""
1. **Cleaning Products:** Ammonia-based cleaning products used in households and commercial settings release ammonia into the air during use.
2. **Consumer Goods:** Certain consumer goods, such as building materials and textiles, may contain ammonia-based compounds that can emit ammonia over time.
""")

st.subheader("Natural Processes:")

st.write("""
1. **Soil and Water:** Natural processes in soil and water, such as microbial decomposition and nitrification, can lead to the release of ammonia into the atmosphere.
""")

st.subheader("Transportation and Storage:")

st.write("""
1. **Ammonia Transport:** The transportation and storage of ammonia for industrial and agricultural purposes can result in accidental releases and emissions.
""")

st.subheader("Livestock Waste Management:")

st.write("""
1. **Manure Management:** Improper handling and storage of animal manure can lead to the release of ammonia into the air as the manure decomposes.
""")
